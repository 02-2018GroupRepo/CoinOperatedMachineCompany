package com.CoinOperatedMachine;

public class Coin {


    final double nickel = 0.05;
    final double dime = 0.10;
    final double quarter = 0.25;

    public Coin() {
    }

    public double getNickel() {
        return nickel;
    }

    public double getDime() {
        return dime;
    }

    public double getQuarter() {
        return quarter;
    }
}
